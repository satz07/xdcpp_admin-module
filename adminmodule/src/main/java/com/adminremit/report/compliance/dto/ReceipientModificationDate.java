package com.adminremit.report.compliance.dto;

public class ReceipientModificationDate {
	
	private String dateOfReceipientAdded;
	private String dateOfReceipientModified;
	
	public String getDateOfReceipientAdded() {
		return dateOfReceipientAdded;
	}
	public void setDateOfReceipientAdded(String dateOfReceipientAdded) {
		this.dateOfReceipientAdded = dateOfReceipientAdded;
	}
	public String getDateOfReceipientModified() {
		return dateOfReceipientModified;
	}
	public void setDateOfReceipientModified(String dateOfReceipientModified) {
		this.dateOfReceipientModified = dateOfReceipientModified;
	}
	
	
}
